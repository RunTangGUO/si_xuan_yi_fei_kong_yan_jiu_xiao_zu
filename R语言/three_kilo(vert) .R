library(ggplot2)
library(dplyr)
library(openxlsx)
timeHMS_formatter<-function(x){
    h<-floor(x/3600)
    m<-floor((x/60)%%60)
    s<-round(x%%60)
    lab<-sprintf("%02d:%02d:%02d",h,m,s)
    lab<-sub("^00:","",lab)
    lab<-sub("^0","",lab)
    return(lab)
}
